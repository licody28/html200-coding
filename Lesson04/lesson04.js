function numberSquared() {
	let input = prompt("Enter a number");
	let result = 0;

	result = Number(input) * Number(input);

	alert(result);
}

function numberMultiplied() {
	let x = prompt("Enter your first number");
	let y = prompt("Enter your second number");
	let bonusResult = 0;

	bonusResult = Number(x) * Number(y);

	alert(bonusResult);
}